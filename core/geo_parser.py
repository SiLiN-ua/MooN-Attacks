import re as _re

# Предкомпилируем паттерны один раз при загрузке модуля.
# Стратегия: левая граница слова (\b) + название города (без правой границы).
# Это позволяет находить склонённые формы:
#   \bновороссийск  → «Новороссийске», «Новороссийска», «Новороссийском» ✓
#   \bтамань        → «Тамань» (точное слово) ✓, но НЕ найдёт «Таманский» (там нет ь) ✓
#   \bднепр         → «Днепра», «Днепре», «Днепром» ✓, НЕ найдёт «Приднепровск» ✓
# Также исключаем ложные срабатывания вида «Новодрупр» т.к. \b требует начало слова.
_CITY_PATTERNS = []
for _city, _coords in CITIES.items():
    # Левая граница слова + название. Правой границы нет — разрешаем падежные окончания.
    _pat = _re.compile(r'\b' + _re.escape(_city), _re.IGNORECASE | _re.UNICODE)
    _CITY_PATTERNS.append((_city, _coords, _pat, len(_city)))

# Сортируем по убыванию длины — длинные названия проверяем первыми
# («Санкт-Петербург» раньше «Петербург», «Новороссийск» раньше «Ростов»)
_CITY_PATTERNS.sort(key=lambda x: x[3], reverse=True)


def extract_city(text: str):
    """Знаходить місто в тексті та повертає (назва, lat, lon) або (None, None, None).

    Використовує ліву межу слова (\b) + підрядок, що дозволяє знаходити відмінкові форми:
    «Новороссийске», «Тамани», «Бердянска» — при цьому НЕ спрацьовує на
    «Приднепровск», «Новоднепровск» тощо.
    Шукає найдовший збіг (сортування по спаданню довжини).
    """
    if not text:
        return None, None, None
    for city, (lat, lon), pattern, _ in _CITY_PATTERNS:
        if pattern.search(text):
            return city.title(), lat, lon
    return None, None, None
